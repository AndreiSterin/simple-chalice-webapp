def information_about(place):
    if place == "London":
        return "London is the capital and largest city of England and the United Kingdom"
    if place == "Oxford":
        return "Oxford is home to the University of Oxford, the oldest university in the English-speaking world"
    if place == "Siegburg":
        return "Siegburg is a small city in the suburbs of Cologne"
    if place == "Exeter":
        return "Exeter is an old Roman fort"
    return f"Unknown place: {place}"
